#include "libft.h"

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (c + 32);
	else
		return (c);
}

int main()
{
	ft_tolower('C');
}
int main()
{
	ft_tolower('D');
}